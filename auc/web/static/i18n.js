let locale = (() => {
  const stored = localStorage.getItem(STORAGE_KEY);
  if (stored === "en" || stored === "zh") return stored;
  return navigator.language?.toLowerCase().startsWith("zh") ? "zh" : "en";
})();

export function getLocale() {
  return locale;
}

export function setLocale(next) {
  locale = next === "en" ? "en" : "zh";
  localStorage.setItem(STORAGE_KEY, locale);
  document.documentElement.lang = locale === "zh" ? "zh-CN" : "en";
  applyI18n();
  window.dispatchEvent(new CustomEvent("auc-locale-change", { detail: locale }));
}

export function toggleLocale() {
  setLocale(locale === "zh" ? "en" : "zh");
}

export function t(key, vars = {}) {
  let s = MESSAGES[locale]?.[key] ?? MESSAGES.zh[key] ?? key;
  for (const [k, v] of Object.entries(vars)) {
    s = s.replaceAll(`{${k}}`, String(v));
  }
  return s;
}

export function applyI18n(root = document) {
  document.documentElement.lang = locale === "zh" ? "zh-CN" : "en";
  root.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  root.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  root.querySelectorAll("[data-i18n-title]").forEach((el) => {
    el.title = t(el.dataset.i18nTitle);
  });
  root.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    el.setAttribute("aria-label", t(el.dataset.i18nAria));
  });
  const langBtn = root.querySelector("#lang-toggle");
  if (langBtn) {
    langBtn.textContent = t("lang.switch");
    langBtn.title = locale === "zh" ? "Switch to English" : "切换到中文";
  }
}
